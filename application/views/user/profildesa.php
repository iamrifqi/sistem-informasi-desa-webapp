                                &nbsp; &nbsp; &nbsp; Luas wilayah Desa Sutapranan adalah 62,850 Ha dengan pemanfaatan Lahan kering diantaranya : Pemukiman dan Pekarangan 50,850 Ha, tanah pertanian/sawah 10,00 Ha dan tanah tegalan 2,0 Ha.
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; Sebagaimana wilayah tropis, Desa Sutapranan mengalami musim kemarau dan musim penghujan dalam tiap tahunnya. Rata-rata perbandingan musim penghujan lebih besar daripada musim kemarau, hal itu disebabkan karena wilayah yang masih hijau dengan vegetasi serta relatif dekat dengan wilayah.
