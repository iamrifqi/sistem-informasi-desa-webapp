                            <h3 class="text-center mb-3">Sejarah Desa</h3>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Asal usul Desa Sutapranan menurut penuturan dari para sesepuh desa bermula dari nama seorang Datuk yang berasal dari Jogjakarta bernama Suta, hal ini ditandai dengan adanya situs makam yang terletak di Desa Sutapranan RT. 07 RW. 02 bernama makam mbah Suta. Karena peranan mbah Suta sangat besar, maka orang-orang terdahulu menyebut wilayah ini dengan sebutan Suta Peranan. Sampai sekarang dinamakan Desa Sutapranan. (wawancara dengan mbah Ratno dan mbah Datowi, 2010)
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Desa Sutapranan terletak di wilayah Kecamatan Dukuhturi, Kabupaten Tegal, yang berbatasan sebelah Barat Desa Bandasari, Utara desa Kademangaran, Timur desa Kaligayam dan Selatan desa Kaligayam kecamatan Talang. Adapun pemerintahan desa Sutapranan dimulai dari mbah Singodiharjo, dan untuk mengenang beliau sekarang diabadikan untuk nama sebuah jalan yang terletak di RT. 08 RW. 02 yaitu jalan perbatasan Desa Sutapranan dengan Kademangaran dengan nama jalan Singodiharjo. Kepemimpinan selanjutnya dipimpin oleh Bapak H. Adbdul Syukur, namun tahun kepemimpinannya tidak diketahui, hanya diperkirakan sebelum Indonesia merdeka. (wawancara dengan mbah Ratno dan mbah Datowi, 2010)
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Selanjutnya, pemerintahan dipegang oleh Bapak H. Sobir hingga tahun 1951 dengan carik anaknya sendiri bernama H. A. Rosyidi. Pada saat itu desa belum mempunyai Sekolah Dasar (SD), hanya ada Madrasah Diniyah Awaliyah (MDA). Setelah H. Sobir Desa sutapranan dipimpin oleh Bapak H. Hasan Bisri (sejak tahun 1951 hingga 1974). Beliau adalah putra dari Bapak H. Abdul Syukur (kepada desa ke-2).
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Pemerintahan selanjutnya, adalah Bapak Yusuf Effendi (putra H. Sobir, mantan kepala desa ke-3). Memerintah desa Sutapranan mulai tahun 1974 sampai dengan tahun 1989. Pada masa pemerintahan ini, mulai ada pemikiran untuk mendirikan sebuah Sekolah Dasar (SD), sehingga didirikanlah Sekolah Dasar yang dibangun di Desa Bandasari, namun hingga berjalannya waktu, karena kondisi lokasi yang sering terjadi banjir, maka dipindahkan di Desa Sutapranan RT. 07 RW. 02 sekarang disebut dengan nama SD Negeri Sutapranan.
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Pada tahun 1990, dilaksanakan kodrah (pemelihan kepala desa secara langsung). Kepala desa Sutapranan terpilih saat itu adalah Bapak H. Fathudin (1990-1998). Beliau adalah putra bapak H. Hasan Bisri (Kepala desa ke-4). Pada saat pemerintahan beliau, desa-desa sering mendapatkan perhatian dari pemerintah pusat dengan memberikan bantuan-bantuan seperti PDMKE (sejenis bantuan pinjaman modal), IDT (Impres Desa Tertinggal), Bandes (Bantuan Desa) yang sekarang dikenal dengan ADD (Alokasi Dana Desa). Setelah pemerintahan H. Fathudin habis, kepala desa selanjutnya adalah Bapak. Moh. Tobir (Tahun 1999 sampai dengan 2007). Pemerintahan sedikit demi sedikit mulai terasa kemajuannya, pembangunan semakin maju.
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Pemerintahan selanjunya, dipimpin oleh bapak H. Muklis. Beliau memimpin selama dua periode. Periode pertama, mulai tahun 2007 sampai dengan 2013 dan periode kedua mulai tahun 2013 sampai tahun 2019. Pada saat pemerintahannya pembenahan-pembenahan terus dilakukan baik dalam segi kelembagaan pemerintahan, pembangunan, perekoniman, kesehatan, pendidikan, social budaya dan lain sebagainya.
                            </p>
                            <p>
                                &nbsp; &nbsp; &nbsp; &nbsp; Demikian sejarah singkat Desa Sutapranan, semoga berguna bagi masyarakat desa Sutapranan pada khususnya dan masyarakat luas pada umumnya.
                            </p>
